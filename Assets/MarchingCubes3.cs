    static Vector3 beginningCoordinates(Vector3 globalChunkOrigin, int blockNum)
    {
        Vector3 localOrigin = new Vector3(0, 0, 0);
        if (blockNum == 0)
            return localOrigin + globalChunkOrigin;
        while (blockNum > 0) {
            int pow = (int)(Mathf.Log(chunkDim, blockNum)); //determines "slot", or which axis the num goes into
            int raised = chunkDim ^ pow;
            int numInSlot = (int)(blockNum / raised);
            switch(pow) {
                case 0:
                    localOrigin.x = numInSlot;
                    break;
                case 1:
                    localOrigin.y = numInSlot;
                    break;
                case 2:
                    localOrigin.z = numInSlot;
                    break;
                default:
                    break;
                    //should not get here, check chunkLength and stuff blocknum, make sure blockNum is at greatest chunkLength^3 - 1
            }
            blockNum -= raised * numInSlot;
        }
        return localOrigin + globalChunkOrigin;
    }

    static Vector3 midpoint(Edge e)
    {
        return new Vector3((e.v1.globalPos.x + e.v2.globalPos.x)/2, (e.v1.globalPos.y + e.v2.globalPos.y) / 2, (e.v1.globalPos.z + e.v2.globalPos.z) / 2);
    }

    private static int indexOf(List<Vector3> list, Vector3 element) //list.find was being buggy and talking about predicates, so..., and I can't do generics here
    {
        for (int i = 0; i < list.Count; i++)
            if (list[i].Equals(element))
                return i;
        return -1;
    }

